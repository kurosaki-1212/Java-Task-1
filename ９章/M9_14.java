public class M9_14 {
    static void sum(int a, int b) {
        System.out.println(a + b);
    }

    static void eve(int a, int b, int c) {
        System.out.println((a + b + c) / 3);
    }

    public static void main(String[] args) {
        int num1 = 734;
        int num2 = 78;

        int num3 = 794;
        int num4 = 710;
        int num5 = 645;
        
        sum(num1, num2);
        eve(num3, num4, num5);

    }
}
